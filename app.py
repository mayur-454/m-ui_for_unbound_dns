def parse_unbound_conf(filepath):
    """Parse unbound config into a dict structure."""
    config = {
        'server': {},
        'forward_zones': [],
        'local_zones': [],
        'local_data': [],
        'access_control': [],
        'rpz': [],
        'raw': ''
    }
    if not os.path.exists(filepath):
        return config
    try:
        with open(filepath, 'r') as f:
            content = f.read()
        config['raw'] = content
        current_section = None
        current_block = {}

        for line in content.splitlines():
            stripped = line.strip()
            if not stripped or stripped.startswith('#'):
                continue

            if stripped == 'server:':
                current_section = 'server'
                continue
            elif stripped == 'forward-zone:':
                if current_section == 'forward-zone' and current_block:
                    config['forward_zones'].append(current_block)
                current_section = 'forward-zone'
                current_block = {'name': '', 'addrs': [], 'tls': False, 'first': False}
                continue
            elif stripped == 'rpz:':
                current_section = 'rpz'
                current_block = {}
                continue

            if ':' in stripped:
                key, _, val = stripped.partition(':')
                key = key.strip()
                val = val.strip().strip('"').strip("'")

                if current_section == 'server':
                    if key == 'access-control':
                        config['access_control'].append(val)
                    elif key == 'local-zone':
                        config['local_zones'].append(val)
                    elif key == 'local-data':
                        config['local_data'].append(val)
                    else:
                        config['server'][key] = val
                elif current_section == 'forward-zone':
                    if key == 'name':
                        current_block['name'] = val
                    elif key == 'forward-addr':
                        current_block['addrs'].append(val)
                    elif key == 'forward-tls-upstream':
                        current_block['tls'] = val.lower() == 'yes'
                    elif key == 'forward-first':
                        current_block['first'] = val.lower() == 'yes'

        if current_section == 'forward-zone' and current_block:
            config['forward_zones'].append(current_block)

    except Exception as e:
        config['error'] = str(e)
    return config


