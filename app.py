@app.route('/api/system/<action>', methods=['POST'])
@login_required
def api_system(action):
    if action == 'reboot':
        run_cmd('shutdown -r +0')
        return jsonify({'success': True, 'message': 'System reboot initiated'})
    elif action == 'restart_networking':
        out, err, rc = run_cmd('systemctl restart networking')
        return jsonify({'success': rc == 0, 'output': out, 'error': err})
    elif action == 'flush_dns':
        out, err, rc = run_cmd('unbound-control reload')
        return jsonify({'success': rc == 0, 'output': out or 'DNS cache flushed', 'error': err})
    elif action == 'flush_cache':
        out, err, rc = run_cmd('unbound-control flush_zone .')
        return jsonify({'success': rc == 0, 'output': out or 'Cache flushed', 'error': err})
    elif action == 'dump_cache':
        out, err, rc = run_cmd('unbound-control dump_cache')
        return jsonify({'success': rc == 0, 'output': out, 'error': err})
    return jsonify({'success': False, 'error': 'Unknown action'})


