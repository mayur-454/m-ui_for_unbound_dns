def get_system_info():
    info = {}

    # Hostname
    info['hostname'] = run_cmd('hostname')[0]
    info['fqdn'] = run_cmd('hostname -f')[0] or info['hostname']
    info['os'] = run_cmd('cat /etc/os-release | grep PRETTY_NAME | cut -d= -f2 | tr -d \'"\'')[0]
    info['kernel'] = run_cmd('uname -r')[0]
    info['arch'] = run_cmd('uname -m')[0]
    info['uptime_raw'] = run_cmd('uptime -p')[0]
    info['uptime_since'] = run_cmd('uptime -s')[0]
    info['load'] = run_cmd("cat /proc/loadavg | awk '{print $1, $2, $3}'")[0]
    info['cpu_count'] = run_cmd('nproc')[0]
    info['cpu_model'] = run_cmd("grep 'model name' /proc/cpuinfo | head -1 | cut -d: -f2")[0].strip()

    # Memory
    mem_out, _, _ = run_cmd("free -b | awk 'NR==2{print $2,$3,$4,$7}'")
    if mem_out:
        parts = mem_out.split()
        if len(parts) >= 3:
            total = int(parts[0])
            used  = int(parts[1])
            free  = int(parts[2])
            avail = int(parts[3]) if len(parts) > 3 else free
            info['mem_total'] = total
            info['mem_used']  = used
            info['mem_free']  = free
            info['mem_avail'] = avail
            info['mem_pct']   = round(used/total*100, 1) if total else 0

    # Disk
    disk_out, _, _ = run_cmd("df -B1 / | tail -1 | awk '{print $2,$3,$4,$5}'")
    if disk_out:
        parts = disk_out.split()
        if len(parts) >= 3:
            info['disk_total'] = int(parts[0])
            info['disk_used']  = int(parts[1])
            info['disk_free']  = int(parts[2])
            info['disk_pct']   = parts[3].strip('%') if len(parts) > 3 else '0'

    # Network interfaces
    ifaces = []
    ip_out, _, _ = run_cmd("ip -o addr show | awk '{print $2, $4}' | grep -v '^lo '")
    for line in ip_out.splitlines():
        parts = line.split()
        if len(parts) >= 2:
            ifaces.append({'name': parts[0], 'addr': parts[1]})
    info['interfaces'] = ifaces

    # Current time
    info['datetime'] = datetime.now().strftime('%Y-%m-%d %H:%M:%S')

    return info


def get_unbound_info():
    info = {}

    # Status
    status_out, _, rc = run_cmd('systemctl is-active unbound')
    info['active'] = status_out.strip() == 'active'
    info['status'] = status_out.strip()

    enabled_out, _, _ = run_cmd('systemctl is-enabled unbound')
    info['enabled'] = enabled_out.strip() == 'enabled'

    # Version
    info['version'] = run_cmd('unbound -V 2>&1 | head -1')[0]

    # PID
    info['pid'] = run_cmd('systemctl show unbound --property=MainPID --value')[0]

    # Config file
    info['config_file'] = get_config_file()

    # Unbound-control stats
    stats = {}
    stat_out, stat_err, stat_rc = run_cmd('unbound-control stats_noreset 2>&1')
    if stat_rc == 0:
        for line in stat_out.splitlines():
            if '=' in line:
                k, _, v = line.partition('=')
                stats[k.strip()] = v.strip()
    info['stats'] = stats

    # Key stats for display
    try:
        info['total_queries']    = int(float(stats.get('total.num.queries', 0)))
        info['cache_hits']       = int(float(stats.get('total.num.cachehits', 0)))
        info['cache_misses']     = int(float(stats.get('total.num.cachemiss', 0)))
        info['prefetch']         = int(float(stats.get('total.num.prefetch', 0)))
        info['recursion_avg']    = round(float(stats.get('total.recursion.time.avg', 0)) * 1000, 2)
        info['cache_hit_pct']    = round(
            info['cache_hits'] / info['total_queries'] * 100, 1
        ) if info['total_queries'] > 0 else 0
        info['num_threads']      = stats.get('total.num.queries_ip_ratelimited', '0')
        info['rrset_cache_count']= int(float(stats.get('rrset.cache.count', 0)))
        info['msg_cache_count']  = int(float(stats.get('msg.cache.count', 0)))
        info['infra_cache']      = int(float(stats.get('infra.cache.count', 0)))
        info['key_cache']        = int(float(stats.get('key.cache.count', 0)))
        info['unwanted_replies'] = int(float(stats.get('unwanted.replies', 0)))
        info['unwanted_queries'] = int(float(stats.get('unwanted.queries', 0)))
    except:
        info['total_queries'] = info['cache_hits'] = info['cache_misses'] = 0
        info['prefetch'] = info['cache_hit_pct'] = 0
        info['recursion_avg'] = 0
        info['rrset_cache_count'] = info['msg_cache_count'] = 0
        info['infra_cache'] = info['key_cache'] = 0
        info['unwanted_replies'] = info['unwanted_queries'] = 0

    # Service timestamps
    started_out, _, _ = run_cmd('systemctl show unbound --property=ActiveEnterTimestamp --value')
    info['started'] = started_out.strip()

    # Memory usage of unbound process
    mem_out, _, _ = run_cmd("ps -o rss= -p $(systemctl show unbound --property=MainPID --value) 2>/dev/null")
    if mem_out.strip():
        try:
            info['mem_kb'] = int(mem_out.strip())
        except:
            info['mem_kb'] = 0
    else:
        info['mem_kb'] = 0

    return info


def get_log_lines(n=100):
    out, _, _ = run_cmd(f'journalctl -u unbound --no-pager -n {n} --output=short-iso 2>/dev/null')
    if not out:
        out, _, _ = run_cmd(f'tail -n {n} /var/log/syslog 2>/dev/null | grep unbound')
    return out


