@app.route('/api/config/save', methods=['POST'])
@login_required
def api_config_save():
    data      = request.get_json()
    cfg_file  = get_config_file()
    conf_text = build_unbound_conf(data)

    # Backup existing
    if os.path.exists(cfg_file):
        backup = cfg_file + '.bak.' + datetime.now().strftime('%Y%m%d%H%M%S')
        try:
            run_cmd(f'cp "{cfg_file}" "{backup}"')
        except:
            pass

    try:
        # Write via tee so it works without root if sudoers configured
        proc = subprocess.run(
            ['tee', cfg_file],
            input=conf_text,
            capture_output=True,
            text=True
        )
        if proc.returncode != 0:
            # Try with sudo
            proc2 = subprocess.run(
                ['sudo', 'tee', cfg_file],
                input=conf_text,
                capture_output=True,
                text=True
            )
            if proc2.returncode != 0:
                return jsonify({'success': False, 'error': proc2.stderr})
    except Exception as e:
        return jsonify({'success': False, 'error': str(e)})

    # Validate config
    _, val_err, val_rc = run_cmd('unbound-checkconf')
    if val_rc != 0:
        return jsonify({'success': False, 'error': 'Config validation failed: ' + val_err})

    return jsonify({'success': True, 'message': 'Configuration saved successfully'})


@app.route('/api/config/apply', methods=['POST'])
@login_required
def api_config_apply():
    """Save + reload unbound"""
    save_result = api_config_save()
    save_data   = save_result.get_json()
    if not save_data.get('success'):
        return save_result

    out, err, rc = run_cmd('systemctl reload-or-restart unbound')
    return jsonify({
        'success': rc == 0,
        'message': 'Configuration saved and Unbound restarted' if rc == 0 else 'Save OK but restart failed',
        'error': err if rc != 0 else ''
    })


